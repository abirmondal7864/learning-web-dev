console.log('Operations in JavaScript');
let a=7;
let b=2;
let sum=a+b;console.log(sum);     //addition
let sub=a-b;console.log(sub);     //subtraction
let mul=a*b; console.log(mul);     //multiplication
let div=a/b; console.log(div);    //division
let rem=a%b; console.log(rem);    //remainder
let pow=a**b; console.log(pow);  //exponentiation
let postin=a++; console.log(postin);   //post increment //a=7
let postdec=a--;  console.log(postdec); //post decrement //a=8
let prein=++a; console.log(prein); //pre increment //a=8
let predec=--a; console.log(predec); //pre decrement //a=7
     
